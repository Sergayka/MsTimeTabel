/**
 * Функция для вычисления четности недели на основе даты
 * Считаем, что отсчет начинается с 2 сентября
 * 02.09 - 08.09 - четная неделя, 09.09 - 15.09 - нечетная и т.д.
 */

// Стартовая дата — 2 сентября
const startDate = new Date(2023, 8, 2); // 2023, 8 (сентябрь, 0-based)

// Функция для вычисления, четная или нечетная неделя
export const getWeekType = (currentDate) => {
    // Разница между текущей датой и стартовой (в миллисекундах)
    const diffTime = currentDate - startDate;

    // Количество дней между текущей датой и стартовой
    const diffDays = Math.floor(diffTime / (1000 * 60 * 60 * 24));

    // Количество недель
    const weeksPassed = Math.floor(diffDays / 7);

    // Если неделя четная, возвращаем "even", если нечетная — "odd"
    return weeksPassed % 2 === 0 ? 'Нечетная неделя' : 'Четная неделя';
};
